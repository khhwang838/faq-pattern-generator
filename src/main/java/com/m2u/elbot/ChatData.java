package com.m2u.elbot;

public class ChatData {

	private String category;
	private String question;
	private String verifyQuestion;
	private String response;
	private String imageUrl;
	private String prevResponse;
	private String recommendQuestion;
	private String textLink;
	
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getQuestion() {
		return question;
	}
	public void setQuestion(String question) {
		this.question = question;
	}
	public String getVerifyQuestion() {
		return verifyQuestion;
	}
	public void setVerifyQuestion(String verifyQuestion) {
		this.verifyQuestion = verifyQuestion;
	}
	public String getResponse() {
		return response;
	}
	public void setResponse(String response) {
		this.response = response;
	}
	public String getImageUrl() {
		return imageUrl;
	}
	public void setImageUrl(String imageUrl) {
		this.imageUrl = imageUrl;
	}
	public String getPrevResponse() {
		return prevResponse;
	}
	public void setPrevResponse(String prevResponse) {
		this.prevResponse = prevResponse;
	}
	public String getRecommendQuestion() {
		return recommendQuestion;
	}
	public void setRecommendQuestion(String recommendQuestion) {
		this.recommendQuestion = recommendQuestion;
	}
	public String getTextLink() {
		return textLink;
	}
	public void setTextLink(String textLink) {
		this.textLink = textLink;
	}
	
}
